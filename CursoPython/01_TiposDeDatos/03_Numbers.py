# Declaración de números
integer = 10
floating = 3.14

# Funciones útiles para números
print(abs(-5))          # Valor absoluto
print(round(3.14159, 2))  # Redondear a 2 decimales
print(pow(2, 3))        # Potencia (2^3)
print(max(1, 2, 3))     # Máximo valor
print(min(1, 2, 3))     # Mínimo valor

# Operaciones básicas
sum_result = integer + floating
print(f"Suma: {sum_result}")
# Declaración de una tupla
coordinates = (10.0, 20.0)

# Funciones útiles para tuplas
print(coordinates[0])  # Acceder a un elemento por índice
print(len(coordinates))  # Longitud de la tupla
print(coordinates.count(10.0))  # Contar ocurrencias de un valor
print(coordinates.index(20.0))  # Índice de un elemento

# Las tuplas son inmutables, no se pueden modificar después de su creación
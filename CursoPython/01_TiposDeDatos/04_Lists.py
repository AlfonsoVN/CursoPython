# Declaración de una lista
fruits = ["manzana", "banana", "cereza"]

# Funciones útiles para listas
fruits.append("naranja")  # Añadir un elemento
print(fruits)
fruits.remove("banana")   # Eliminar un elemento
print(fruits)
print(fruits[1])          # Acceder a un elemento por índice
print(len(fruits))        # Longitud de la lista
print(fruits.index("cereza"))  # Índice de un elemento
